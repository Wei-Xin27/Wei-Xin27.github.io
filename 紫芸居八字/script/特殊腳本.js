function 測八字條件(){
    //1. 全陽、全陰

    var YYDate;
    var theDate="01011924";
    var daycount = (10*31*12*6)+100;
    var CountDate, DateDt, GZDate;
    var StartDate=日期值轉換("04021984231900", 1);  //1924: "06021924095000"; 1984: "04021984231900"
    var EndDate=日期值轉換("15022037000000", 1); //1924: "04021984231900"; 1984: "15022037"
    var 地支純陽=["寅","申"];
    var 地支純陰=["丑","卯","未","酉"];
    var 年柱, 月柱, 日柱, 時柱, 年干陰陽, 月干陰陽, 日干陰陽, 時干陰陽;
    var TimePack=["003000", "020000", "040000", "060000", "080000", "100000", "120000", "140000", "160000", "180000", "200000", "220000", "233000"];
    var 全陽八字=[], 全陰八字=[];
    var YangCounter=1;
    var YinCounter=1;
    //var YYDate=StartDate;
    var CurYear=StartDate.getFullYear();
    do {
        for(var j=0; j<13; j++){
            var CountDate=theDate+TimePack[j];
            YYDate = 日期值轉換(CountDate, 1);

            if(YYDate>=StartDate && YYDate<EndDate){
                YYDateDt = 西曆轉換(YYDate);
                YYGZDate=YYDateDt[0][0];
                
                年柱=YYGZDate.substr(0,2);
                月柱=YYGZDate.substr(2,2);
                日柱=YYGZDate.substr(4,2);
                時柱=YYGZDate.substr(6,2);

                年干陰陽=天干屬性(年柱.substr(0,1),"陰陽");
                月干陰陽=天干屬性(月柱.substr(0,1),"陰陽");
                日干陰陽=天干屬性(日柱.substr(0,1),"陰陽");
                時干陰陽=天干屬性(時柱.substr(0,1),"陰陽");

                //************************* 條 件 ************************* 
                if(年干陰陽=="陽" && 月干陰陽=="陽" && 日干陰陽=="陽" & 時干陰陽=="陽") {
                    var 全純陽="全陽";
                    if((地支純陽.indexOf(年柱.substr(1,1)) >=0) && (地支純陽.indexOf(月柱.substr(1,1)) >=0) && (地支純陽.indexOf(日柱.substr(1,1)) >=0) && (地支純陽.indexOf(時柱.substr(1,1)) >=0)) 全純陽="純陽";
                    var PrnYear=YYDate.getFullYear();
                    var PrnMonth=YYDate.getMonth()+1;
                    var PrnDay=YYDate.getDate();
                    var PrnHour=YYDate.getHours();
                    var PrnDate=PrnYear+"-"+PrnMonth+"-"+PrnDay+" : "+PrnHour;
                    var PrnStatement = YangCounter +", " + PrnDate + ", " + 年柱+" " +月柱 + " "+日柱+" " +時柱 + ", " + 全純陽;
                    YangCounter ++;
                    //console.log(PrnDate+", "+年柱+月柱+日柱+時柱);
                    全陽八字.push(PrnStatement);
                } // end if yang

                if(年干陰陽=="陰" && 月干陰陽=="陰" && 日干陰陽=="陰" & 時干陰陽=="陰") {
                    var 全純陰="全陰";
                    if((地支純陰.indexOf(年柱.substr(1,1)) >=0) && (地支純陰.indexOf(月柱.substr(1,1)) >=0) && (地支純陰.indexOf(日柱.substr(1,1)) >=0) && (地支純陰.indexOf(時柱.substr(1,1)) >=0)) 全純陰="純陰";
                    var PrnYear=YYDate.getFullYear();
                    var PrnMonth=YYDate.getMonth()+1;
                    var PrnDay=YYDate.getDate();
                    var PrnHour=YYDate.getHours();
                    var PrnDate=PrnYear+"-"+PrnMonth+"-"+PrnDay+" : "+PrnHour;
                    var PrnStatement = YinCounter +", " + PrnDate + ", " + 年柱+" " +月柱 + " "+日柱+" " +時柱 + ", " + 全純陰;
                    YinCounter ++;
                    //console.log(PrnDate+", "+年柱+月柱+日柱+時柱);
                    全陰八字.push(PrnStatement);
                } // end if yang
                //************************************************************

            }  // if YYdate > theDate
        } // end for j
        //console.log(YYDate);
        YYDate.setDate(YYDate.getDate()+1);
        theDate=zeroPad(YYDate.getDate(), 2) + zeroPad(YYDate.getMonth()+1, 2) + YYDate.getFullYear();
        CurYear=YYDate.getFullYear();
    } while (YYDate < EndDate); //(YangCounter < 1000);

    // *********** grap the Div tag to output ***********
    $("#qyang").html(全陽八字.join("<br>"));
    $("#qyin").html(全陰八字.join("<br>"));
}


