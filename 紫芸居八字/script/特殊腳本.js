function 流年十二神煞(農曆生年, 農曆生月, 農曆生日, 農曆生時, 選項){
    var 流年虛歲=new Date().getFullYear()-農曆生年+1;
    //var 流年虛歲=2014-農曆生年+1;
    var 流年神煞矩陣=[];
    var 流年神煞註解=[];
    var 年神煞數=流年虛歲%12;
    var 月神煞數=(年神煞數+農曆生月-1)%12;
    var 日神煞數=(月神煞數+農曆生日-1)%12;
    var 生時=農曆生時;
    if(生時=="早子" || 生時=="夜子") 生時="子"
    var 時數=地支字轉值(生時);
    if (時數==0) 時數=12;  //亥時
    var 時神煞數=(日神煞數+時數-1)%12;
    
    var 押運神=十二神煞(年神煞數);  //年
    var 串宮神=十二神煞(月神煞數);  //月
    var 守護神=十二神煞(日神煞數);  //日
    var 巡門神=十二神煞(時神煞數);  //時
    
    switch(選項){
        case 1: case "神煞":
            流年神煞矩陣.push(押運神.名稱);
            流年神煞矩陣.push(串宮神.名稱);
            流年神煞矩陣.push(守護神.名稱);
            流年神煞矩陣.push(巡門神.名稱);
            return 流年神煞矩陣;
        case 2: case "流年十二神煞論斷":
            //var 十二神煞註解;
            //十二神煞註解="押運神："+押運神.名稱+"（"+押運神.吉凶+"）："+押運神.註解1+押運神.註解2;
            流年神煞註解.push("押運神："+押運神.名稱+"（"+押運神.吉凶+"）："+押運神.註解1+押運神.註解2);
            流年神煞註解.push("串宮神："+串宮神.名稱+"（"+串宮神.吉凶+"）："+串宮神.註解1+串宮神.註解2);
            流年神煞註解.push("守護神："+守護神.名稱+"（"+守護神.吉凶+"）："+守護神.註解1+守護神.註解2);
            流年神煞註解.push("巡門神："+巡門神.名稱+"（"+巡門神.吉凶+"）："+巡門神.註解1+巡門神.註解2);
            //console.log(流年神煞註解.join("<br><br>"));
            return 流年神煞註解.join("<br><br>");
    } //end switch 選項
}

