    var 雜項論命註解矩陣 = [];

    雜項論命註解矩陣.push("壽生經：<br>" + 雜項論命.壽生經.判斷句+"。");
    雜項論命註解矩陣.push("袁天罡稱骨算命：<br>" + 雜項論命.稱骨算命.重量文字計算 + "<br>歌訣：" + 雜項論命.稱骨算命.歌訣);
    雜項論命註解矩陣.push("二十八宿批命：<br>" + 雜項論命.二十八宿批命);
    
    if(雜項論命.神煞.童子煞.成立) 雜項論命註解矩陣.push("童子煞：<br>" + 雜項論命.神煞.童子煞.註解);
    if(雜項論命.神煞.鐵掃帚.成立) 雜項論命註解矩陣.push("鐵掃帚：<br>" + 雜項論命.神煞.鐵掃帚.註解);
    if(雜項論命.神煞.天掃星.成立) 雜項論命註解矩陣.push("天掃星：<br>" + 雜項論命.神煞.天掃星.註解);
    if(雜項論命.神煞.地掃星.成立) 雜項論命註解矩陣.push("地掃星：<br>" + 雜項論命.神煞.地掃星.註解);
    if(雜項論命.神煞.四廢日.成立) 雜項論命註解矩陣.push("四廢日：<br>" + 雜項論命.神煞.四廢日.註解);
    if(雜項論命.神煞.八專日.成立) 雜項論命註解矩陣.push("八專日：<br>" + 雜項論命.神煞.八專日.註解);
    if(雜項論命.神煞.九丑日.成立) 雜項論命註解矩陣.push("九丑日：<br>" + 雜項論命.神煞.九丑日.註解);
    if(雜項論命.神煞.陰陽差錯日.成立) 雜項論命註解矩陣.push("陰陽差錯日：<br>" + 雜項論命.神煞.陰陽差錯日.註解);
    if(雜項論命.神煞.十惡大敗日.成立) 雜項論命註解矩陣.push("十惡大敗日：<br>" + 雜項論命.神煞.十惡大敗日.註解);
    
    if(雜項論命.納音.論斷.成立) 雜項論命註解矩陣.push("納音論命：<br>" + 雜項論命.納音.論斷.註解);
    
    雜項論命.總結 = 雜項論命註解矩陣.join("<br><br>");
    
