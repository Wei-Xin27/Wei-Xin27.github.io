    雜項論命.納音 = {};
    雜項論命.納音.論斷 = 盲派納音_總論(輸入資料);

    
    // *** 三 世 論 命 ***
    var 三世書命盤 = JSON.parse(JSON.stringify(輸入資料));
    delete 三世書命盤.運算西曆; delete 三世書命盤.城市; delete 三世書命盤.節氣用區域時間; delete 三世書命盤.節氣用軌道太陽時;
    三世書命盤.四柱 = 三世書命盤.干支曆.四柱;
    三世書命盤.生年支 = 三世書命盤.農曆.干支年.substr(1,1);
