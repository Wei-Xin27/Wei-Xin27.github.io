		var OverallMinutesDiff=(BaseLong-CityLatLongData.Long)*4;  //每一度差別4分鐘
		var minDiff=Math.floor(OverallMinutesDiff);
		var secDiff=Math.floor((OverallMinutesDiff-minDiff)*60);
