function 庫馬花架構(命盤){
    var 地支矩陣 = [命盤.年支.地支, 命盤.月支.地支, 命盤.日支.地支, 命盤.時支.地支];
    var 庫量=0, 馬量=0, 花量=0;

    for (i = 0; i < 地支矩陣.length; i++){
        if (地支矩陣[i] == "辰" || 地支矩陣[i] == "戌" || 地支矩陣[i] == "丑" || 地支矩陣[i] == "未") 庫量++;
        if (地支矩陣[i] == "子" || 地支矩陣[i] == "午" || 地支矩陣[i] == "卯" || 地支矩陣[i] == "酉") 花量++;
        if (地支矩陣[i] == "寅" || 地支矩陣[i] == "申" || 地支矩陣[i] == "巳" || 地支矩陣[i] == "亥") 馬量++;
    }

    this.庫 = 庫量;
    this.馬 = 馬量;
    this.花 = 花量;
}

