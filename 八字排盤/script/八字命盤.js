    var 日加=Number(起運字串.substr(4,2));
    var 上運日期=new Date(西曆生日);
	
    //console.log(西曆生日 + ", " + 起運字串+", " + 年加);
    上運日期.setFullYear(上運日期.getFullYear()+年加);
	上運日期.setMonth(上運日期.getMonth()+月加);
	上運日期.setDate(上運日期.getDate()+日加);
	
	var 生年干支=六十干支屬性((上運日期.getFullYear()-1923) % 60, "干支");
	var 下個換運天干=天干數轉字(天干滾動(天干字轉值(生年干支.substr(0,1)), 5));
    var 上運字串="陽曆" + 上運日期.getFullYear() + "年（" + 生年干支 + "）" + (上運日期.getMonth()+1) + "月" + 上運日期.getDate()+ "日後上運";
